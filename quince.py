# 15. Pide dos números y muestra si el primero es menor o igual que el segundo (usar operador de comparación).

numero1 = int(input("Ingrese numero: "))
numero2 = int(input("Ingrese numero: "))

menor_o_igual_que = numero1 <= numero2
print(f"el numero 1 es menor o igual al numero 2: {menor_o_igual_que}")
